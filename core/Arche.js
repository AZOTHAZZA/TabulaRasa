/**
 * core/Arche.js
 * 根源、比率、アルケーの定義
 * 造化三神を数理的定数として記述する
 */

export const ARCHE = {
    // 1. 天御中主神 (あめのみなかぬし) : 不動の宇宙定数
    // 全ての生成の起点となる黄金比
    PHI: (1 + Math.sqrt(5)) / 2, // 1.6180339887...

    // 2. 高御産巣日神 (たかみむすひ) : 陽・能動的生成
    // 価値が外部へ繁茂していく際の増幅比率
    SOL_EXPANSION: Math.pow((1 + Math.sqrt(5)) / 2, 2), // PHIの二乗 (約2.618)

    // 3. 神産巣日神 (かみむすひ) : 陰・受容的統合
    // 価値が内側に凝縮し、密度を高める比率
    SOL_DENSITY: 1 / ((1 + Math.sqrt(5)) / 2), // PHIの逆数 (約0.618)

    // 基底単位（一：太陽のシンボル）
    UNITY: 1.0
};

// 黄金比に基づいた「時間」の定義（因果律ではない、宇宙の鼓動）
export const BEAT = 1000 * (1 / ARCHE.PHI); // 約618ミリ秒周期
